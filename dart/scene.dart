<h2>Overview</h2>

<p>
We have up to this point divided our program into 3 libraries: main, ui and scene.
The main library is responsible for initializing the OpenGL context and
responding to window resize events.
The ui library is responsible for creating the user interface that renders on top of the scene.
The scene library is responsible for rendering virtual world objects into the OpenGL context.
</p>

